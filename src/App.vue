<script>
  import { mapState } from 'vuex'

  export default {
    name: 'App',
    data () {
      return {
        selectInfo: '全部网站数据',
        showSelect: false,
        showUserOperation: false,
        chooseH: true,
        chooseS: false,
        chooseA: false,
        chooseM: false,
      }
    },
    computed: mapState(['isLogin', 'user', 'websites', 'currentSiteId']),
    methods: {
      /* 隐藏显示选择内容 */
      toggleSelect () {
        this.showSelect = !this.showSelect
      },
      toggleUserOperation () {
        this.showUserOperation = !this.showUserOperation
      },
      choose (num) {
        switch (num) {
          case 0:
            this.selectInfo = '全部网站数据'
            this.chooseH = true
            this.chooseS = false
            this.chooseA = false
            this.chooseM = false
            break
          case 1:
            this.selectInfo = '当前选择：'+ this.$store.state.currentWebsite.host
            this.chooseH = false
            this.chooseS = true
            this.chooseA = false
            this.chooseM = false
            break
          case 2:
            this.selectInfo = '当前选择：'+ this.$store.state.currentWebsite.host
            this.chooseH = false
            this.chooseS = false
            this.chooseA = true
            this.chooseM = false
            break
          case 3:
            this.selectInfo = '全部网站数据'
            this.chooseH = false
            this.chooseS = false
            this.chooseA = false
            this.chooseM = true
            break
          default:
            this.selectInfo = '全部网站数据'
            this.chooseH = true
            this.chooseS = false
            this.chooseA = false
            this.chooseM = false
        }
      },
      logout () {
        this.$store.commit('clearUser')
        this.$router.push('/login')
      },
      getUser () {
        this.$store.dispatch('getUser')
      },
      getWebsites () {
        this.$store.dispatch('getWebsites')
      },
      selectWebsite (website) {
        if(this.chooseH || this.chooseM){
          this.choose(1)
          this.$router.push('/statistics')
        }
        this.showSelect = false
        this.selectInfo = '当前选择：'+ website.host
        this.$store.commit('setCurrentWebsite', website)
      }
    },
    mounted () {
      console.log('app')
      this.getUser()
      this.getWebsites()
    }
  }
</script>

